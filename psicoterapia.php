                        <p>A pesquisa dos efeitos da psicoterapia, baseada nos padrões da pesquisa farmacêutica, busca diferenciar o efeito da terapia em si, por um lado, e o efeito placebo (ou seja, a melhora nos sintomas devido à expectativa do paciente, e não à terapia em si) e a remissão espontânea (ou seja, a cura dos sintomas por si sós), por outro lado. Uma resposta à questão do efeito da psicoterapia não se dá no entanto, com apenas meia dúzia de estudos; pelo contrário, são necessários muitos deles, que são então reunidos em uma meta-análise, ou seja, um estudo que reúne e resume um grande número de estudos. Com base em várias meta-análises, pode-se afirmar, hoje, que a psicoterapia, pelo menos em suas formas tradicionais, é realmente efetiva - ou seja, tem efeitos mais fortes sobre a saúde psíquica do que o efeito placebo e a remissão espontânea. Cabe, no entanto, observar, que a questão do efeito placebo se apresenta de maneira diferente em psicoterapia e em farmácia, pois, enquanto nesta se trata de um efeito indesejável (se quer de fato que o medicamento funcione por si mesmo), em psicoterapia trata-se de um forte efeito psicológico, que deve ser compreendido e que pode ser utilizado como parte da própria terapia..
